fourInt = int(input("Please enter 1, 2, 3 or 4:"))
if (fourInt == 1):
    print("ONE")
else:
    if (fourInt == 2):
        print("TWO")
    else:
        if (fourInt == 3):
            print("THREE")
        else:
            if (fourInt == 4):
                print("FOUR")
            else:
                print("ERROR - Please enter 1, 2, 3 or 4")
