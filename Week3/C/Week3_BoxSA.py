boxHeight = float(input("Please enter the height of the box in metres."))
boxWidth = float(input("Please enter the width of the box in metres."))
boxLength = float(input("Please enter the length of the box in metres."))
surfaceArea = boxHeight*boxWidth*boxLength

print("Height = %.2f metres" %boxHeight)
print("Width = %.2f metres" %boxWidth)
print("Length = %.2f metres" %boxLength)
print("Surface area = %.2f metres" %surfaceArea)
